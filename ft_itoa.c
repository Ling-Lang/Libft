#include "libft.h"

int	len(long nb)
{
	int	len;

	len = 0;
	if (nb < 0)
	{
		nb = nb * -1;
		len++;
	}
	while (nb > 0)
	{
		nb = nb / 10;
		len++;
	}
	return (len);
}

char	*ft_itoa(int nb)
{
	char	*str;
	int		i;

	i = len(nb);
	str = (char *)malloc(sizeof(char) * (i + 2));
	if (!str)
		return (NULL);
	str[i--] = '\0';
	if (nb == 0)
		return ("0");
	if (nb == -2147483648)
		return ("-2147483648");
	if (nb < 0)
	{
		str[0] = '-';
		nb = nb * -1;
	}
	while (nb > 0)
	{
		str[i] = 48 + (nb % 10);
		nb = nb / 10;
		i--;
	}
	return (str);
}
